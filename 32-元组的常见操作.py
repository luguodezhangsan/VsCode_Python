# 元组只支持查找操作
t1 = ('a', 'b', 'c', 'd')
# 1. 按下标查找数据
print(t1[1])  # b

# 2. index()查找某个数据
print(t1.index('c'))  # 2

# 3. count()统计某个数据出现次数
print(t1.count('d'))  # 1

# 4. len()统计元组中数据的个数
print(len(t1))  # 4

# t1[0] = 'aaa' -- 会直接报错，不能修改
t2 = ('a', 'b', ['c', 'd'])
print(t2[2])  # ['c', 'd']
print(t2[2][0])  # c
t2[2][0] = '1'
print(t2)  # ('a', 'b', ['1', 'd'])
# 说明元组里第一层数据不能修改，修改会直接报错，若元组数据里面有列表，则修改列表里面的数据是支持的，故不要轻易修改第一层数据