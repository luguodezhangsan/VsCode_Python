# 变量名 = 值
# = 的意思转变为“赋值”,存储数据

# 2.1 标识符的命名规则：
'''
1.由数字、字母、下划线组成
2.不能数字开头
3.不能使用内置关键词:False、True、and、as、for等
4.严格区分大小写
'''

# 2.2 命名习惯
'''
1.见名知义
2.大驼峰:每个单词首字母都大写，例如:MyName
3.小驼峰:第二个含第二个以后的单词大写,例如:myName
4.下划线:my_name
'''

# 2.3 使用变量
a = 1
print(a)

Name = '小红'
print(Name)

myName = 'Tom'
print(myName)

